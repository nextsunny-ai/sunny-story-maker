def get_genre(code_or_letter: str) -> dict:
    """장르 데이터 조회"""
    if code_or_letter in GENRES:
        return GENRES[code_or_letter]
    for letter, data in GENRES.items():
        if data["code"] == code_or_letter:
            return data
    return None


def list_genre_names() -> list[str]:
    """드롭다운용 장르 이름 리스트"""
    return [f"{letter}. {data['name']} ({data['subtitle']})" for letter, data in GENRES.items()]


def parse_genre_choice(choice: str) -> str:
    """드롭다운 선택값에서 letter 추출"""
    return choice.split(".")[0].strip() if "." in choice else choice
